    private void JB_ImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_ImprimirActionPerformed
        
         DefaultTableModel modelo = (DefaultTableModel)Tabela.getModel();
        String[] linha;
        modelo.setNumRows(0); //Limpa a tabela
        String tipo;
        for (Funcionario p : funcionario){
            if (p instanceof Terceirizado)
                tipo = "Terceirizado";
            else if (p instanceof Gerente)
                tipo = "Gerente";
            else
                tipo = "Contratado";
                        
            linha = new String[]{p.getNome(),tipo};
            modelo.addRow(linha);        
        }
        
        
    }//GEN-LAST:event_JB_ImprimirActionPerformed

