    private void JB_PesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_PesquisarActionPerformed
      
         if (TF_Cod.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Código deve estar preenchido!");
        else
         {
              p = existeFuncionario(Integer.parseInt(TF_Cod.getText()));
              limpar();
              if(p!=null)
              {
                  TF_Cod.setText(p.getCod()+"");
                  TF_Nome.setText(p.getNome());
                  TF_Email.setText(p.getEmail());
                  if(p instanceof Terceirizado)
                  {
                      RB_T.setSelected(true);
                      Selecionar();
                      
                      if(((Terceirizado) p).getInicioC()!=null)
                      {    
                      CB_I_D.setSelectedItem(((Terceirizado) p).getInicioC().get(Calendar.DAY_OF_MONTH)+"");
                      CB_I_M.setSelectedItem(((Terceirizado) p).getInicioC().get(Calendar.MONTH)+"");
                      CB_I_A.setSelectedItem(((Terceirizado) p).getInicioC().get(Calendar.YEAR)+"");
                      }
                      if(((Terceirizado) p).getTerminoC()!=null)
                      {
                      CB_F_D.setSelectedItem(((Terceirizado) p).getTerminoC().get(Calendar.DAY_OF_MONTH)+"");
                      CB_F_M.setSelectedItem(((Terceirizado) p).getTerminoC().get(Calendar.MONTH)+"");
                      CB_F_A.setSelectedItem(((Terceirizado) p).getTerminoC().get(Calendar.YEAR)+"");
                      }
                  }else
                  {
                      RB_C.setSelected(true);
                     Selecionar();
                     TF_CTPS.setText(((Contratado) p).getCtps());
                     
                   
                     
                     
                    if(((Contratado) p).getDataAdm()!=null){
                        
                      
                         
                      CB_A_D.setSelectedItem(((Contratado) p).getDataAdm().get(Calendar.DAY_OF_MONTH)+"");
                      CB_A_M.setSelectedItem(((Contratado) p).getDataAdm().get(Calendar.MONTH)+"");
                      CB_A_A.setSelectedItem(((Contratado) p).getDataAdm().get(Calendar.YEAR)+"");
                     }
                       if(((Contratado) p).getDataDem()!=null){
                      CB_D_D.setSelectedItem(((Contratado) p).getDataDem().get(Calendar.DAY_OF_MONTH)+"");
                      CB_D_M.setSelectedItem(((Contratado) p).getDataDem().get(Calendar.MONTH)+"");
                      CB_D_A.setSelectedItem(((Contratado) p).getDataDem().get(Calendar.YEAR)+"");                    
                       }
                       
                       
                     if(p instanceof Gerente)
                  {
                      
                      CB_G.setSelected(true);
                      CB_T.setSelectedItem(((Gerente) p).getTitulacao());
                  }
                  }
                     JB_Excluir.setEnabled(true);
                JB_Alterar.setEnabled(true);
                  
              }else
             JOptionPane.showMessageDialog(null, "Funcionario não encontrado!");
         
         }
        
        
        
    }//GEN-LAST:event_JB_PesquisarActionPerformed

