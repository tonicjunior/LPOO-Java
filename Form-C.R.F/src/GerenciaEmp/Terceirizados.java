    public Terceirizados() {
        initComponents();
        
        DefaultTableModel modelo = (DefaultTableModel)Tabela1.getModel();
        String[] linha;
        modelo.setNumRows(0); //Limpa a tabela
        String tipo="-",tipo2="-";
       
          
            
        for (Funcionario p : funcionario){
            if (p instanceof Terceirizado)
                {   
                    if(((Terceirizado) p).getInicioC()!=null)
                    {
                        tipo=((Terceirizado) p).getInicioC().get(Calendar.DAY_OF_MONTH)+"/"+
                               ((Terceirizado) p).getInicioC().get(Calendar.MONTH)+"/"+
                               ((Terceirizado) p).getInicioC().get(Calendar.YEAR) ;
                    }
                        if(((Terceirizado) p).getTerminoC()!=null)
                        {
                                tipo2=((Terceirizado) p).getTerminoC().get(Calendar.DAY_OF_MONTH)+"/"+
                               ((Terceirizado) p).getTerminoC().get(Calendar.MONTH)+"/"+
                               ((Terceirizado) p).getTerminoC().get(Calendar.YEAR) ;
                        }
                  
                linha = new String[]{p.getCod()+"",p.getNome(),tipo, tipo2};
                
                modelo.addRow(linha);  
                }
            
            tipo="-";
            tipo2="-";
            }
                  
        
        
        
        
        
       
    }

